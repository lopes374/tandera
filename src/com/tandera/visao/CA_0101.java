    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        // TODO add your handling code here:
        String descr = ""+tabela.getValueAt(tabela.getSelectedRow(), 0);
        conexao.conexao();
        conn.conexao();
        conexao.executaSQL("select * from fornecedor where id_for =" + descr);
        try {
            conexao.rs.first();
            jTextFieldCod.setText(String.valueOf(conexao.rs.getInt("id_for")));
            jTextFieldNome.setText(conexao.rs.getString("nom_for"));
            jTextFieldEndereco.setText(conexao.rs.getString("end_for"));
            jTextFieldNumero.setText(conexao.rs.getString("num_for"));
            jTextFieldBairro.setText(conexao.rs.getString("bai_for"));
            PesquisaEstado(conexao.rs.getInt("cestado"));
            PesquisaCidade(conexao.rs.getInt("ccidade"));
            jFormattedTextFieldCEP.setText(conexao.rs.getString("cep_for"));
            jTextFieldPais.setText(conexao.rs.getString("pais_for"));
            jFormattedTextFieldCNPJ.setText(conexao.rs.getString("cnpj_for"));
            jTextFieldEstadual.setText(conexao.rs.getString("est_for"));
            jTextFieldMunicipal.setText(conexao.rs.getString("mun_for"));
            jFormattedTextFieldTelefone.setText(conexao.rs.getString("tel_for"));
            jTextFieldEmail.setText(conexao.rs.getString("email_for"));
            jButtonAlterar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao selecionar os dados!\nERRO:" + ex);
        }
    }//GEN-LAST:event_tabelaMouseClicked

