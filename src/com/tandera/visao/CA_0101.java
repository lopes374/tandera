    public void preencherComboEstado() {    
        conexao.executaSQL("select * from estado order by descr_estado");
        jComboBoxEstado.removeAllItems();// remove todos os itens da combobox
        try{
            conexao.rs.first();
            do {
                jComboBoxEstado.addItem(conexao.rs.getString("descr_estado"));
            } while (conexao.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher combobox:" + ex);
        }
        return;
    }
    
